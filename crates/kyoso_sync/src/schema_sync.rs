/// Edge-side counterpart to [`SchemaSyncedNodeComponentPlugin`]. Add
/// one per edge component that should sync via a typed schema.
///
/// ```ignore
/// app.add_plugins(SchemaSyncedEdgeComponentPlugin::<N, E, GraphEdge>::default());
/// ```
pub struct SchemaSyncedEdgeComponentPlugin<N, E, C> {
    _phantom: PhantomData<fn() -> (N, E, C)>,
}

impl<N, E, C> Default for SchemaSyncedEdgeComponentPlugin<N, E, C> {
    fn default() -> Self {
        Self {
            _phantom: PhantomData,
        }
    }
}

impl<N, E, C> Plugin for SchemaSyncedEdgeComponentPlugin<N, E, C>
where
    N: Syncable,
    E: Syncable,
    C: SchemaSync,
    <C::Schema as Crdt>::Delta: IntoWireOp,
    <C::Schema as Crdt>::Mutation: Send + Sync + 'static,
{
    fn build(&self, app: &mut App) {
        app.init_resource::<SchemaDoc<C::Schema>>();
        // Same ordering rationale as the node plugin: detect first
        // (capture user edits before inbound apply / projection might
        // overwrite them); detect's echo guard skips just-added default
        // components. Then apply inbound + project doc → component.
        app.add_systems(
            Update,
            (
                ensure_edge_schema_slots::<C>,
                detect_typed_edge_changes::<N, E, C>,
                route_typed_edge_inbound::<C>,
                project_typed_edge_to_bevy::<C>,
            )
                .chain()
                .after(crate::plugin::detect_added_edges::<N, E>)
                .before(crate::plugin::outbound_system::<N, E>),
        );
    }
}

fn ensure_edge_schema_slots<C>(
    mut doc: ResMut<SchemaDoc<C::Schema>>,
    index: Res<EntityCrdtIndex>,
    has_c: Query<Entity, With<C>>,
) where
    C: SchemaSync,
    <C::Schema as Crdt>::Delta: IntoWireOp,
    <C::Schema as Crdt>::Mutation: Send + Sync + 'static,
{
    // Same rationale as `ensure_schema_slots` — see its docstring.
    for entity in has_c.iter() {
        if let Some(id) = index.edge_id(entity) {
            doc.ensure_node(id);
        }
    }
}

fn detect_typed_edge_changes<N, E, C>(
    mut engine: ResMut<ClientSyncEngine>,
    doc: Res<SchemaDoc<C::Schema>>,
    index: Res<EntityCrdtIndex>,
    components: Query<(Entity, Ref<C>), Changed<C>>,
) where
    N: Syncable,
    E: Syncable,
    C: SchemaSync,
    <C::Schema as Crdt>::Delta: IntoWireOp,
    <C::Schema as Crdt>::Mutation: Send + Sync + 'static,
{
    for (entity, component) in components.iter() {
        let Some(edge_id) = index.edge_id(entity) else {
            continue;
        };
        let Some(current) = doc.node(edge_id) else {
            continue;
        };
        let mutations = component.changes_against(current);
        if mutations.is_empty() {
            continue;
        }
        let mut throwaway = current.clone();
        for mutation in mutations {
            let op_id = engine.next_id();
            let mut state = kyoso_crdt::CausalState::new();
            let mut ctx = kyoso_crdt::CausalContext::new(op_id, None, &mut state);
            let typed_delta = throwaway.mutate(mutation, &mut ctx);
            let (inner_path, wire) = typed_delta.into_wire_op();
            let mut path = Path::field(C::SCHEMA_NAME);
            for seg in inner_path.0 {
                path.0.push(seg);
            }
            engine.enqueue(Op::new(
                op_id,
                OpKind::SetRefEdgeProperty {
                    target: edge_id,
                    path,
                    delta: wire,
                },
            ));
        }
    }
}

fn route_typed_edge_inbound<C>(
    mut doc: ResMut<SchemaDoc<C::Schema>>,
    mut events: MessageReader<RemoteOpApplied>,
) where
    C: SchemaSync,
    <C::Schema as Crdt>::Delta: IntoWireOp,
    <C::Schema as Crdt>::Mutation: Send + Sync + 'static,
{
    for ev in events.read() {
        let op = &ev.0;
        let OpKind::SetRefEdgeProperty { target, path, delta } = &op.kind else {
            continue;
        };
        let Some(PathSegment::Field(head)) = path.0.first() else {
            continue;
        };
        if head != C::SCHEMA_NAME {
            continue;
        }
        let inner_path = Path(path.0[1..].to_vec());
        let stripped = Op {
            id: op.id,
            seq: op.seq,
            kind: OpKind::SetRefEdgeProperty {
                target: *target,
                path: inner_path,
                delta: delta.clone(),
            },
        };
        if let Err(e) = doc.apply_property_op(&stripped) {
            tracing::warn!(?e, schema = %C::SCHEMA_NAME, "edge schema apply rejected op");
        }
    }
}

fn project_typed_edge_to_bevy<C>(
    mut commands: Commands,
    doc: Res<SchemaDoc<C::Schema>>,
    index: Res<EntityCrdtIndex>,
    mut components: Query<&mut C>,
) where
    C: SchemaSync,
    <C::Schema as Crdt>::Delta: IntoWireOp,
    <C::Schema as Crdt>::Mutation: Send + Sync + 'static,
{
    if !doc.is_changed() {
        return;
    }
    for (entity, id) in index.edge_of_entity.iter() {
        let Some(schema) = doc.node(*id) else { continue };
        match components.get_mut(*entity) {
            Ok(mut component) => component.write_back(schema),
            Err(_) => {
                let schema = schema.clone();
                commands.queue(InsertSchemaProjected::<C> {
                    entity: *entity,
                    schema,
                    _ph: PhantomData,
                });
            }
        }
    }
}
